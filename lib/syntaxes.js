/**
 * @param {SyntaxConfig[]} sa
 * @returns {SyntaxFragment}
 * @throws {Error}
 */
function compute(sa) {
	/** @type {Record<string, SyntaxFragment>} */
	let t = {}

	for (let s of sort(sa)) {
		if (!s.extends && !s.colors) {
			throw new Error(`Syntax '${s.id}' has no token colors`)
		}

		/** @type {SyntaxFragment} */
		let x = {tokenColors: []}

		if (s.colors) {
			x.tokenColors = transform(s.colors)
		} else if (s.extends) {
			let y = t[s.extends.id]
			if (!y) {
				throw new Error(`Syntax '${s.extends.id}' is not found`)
			}

			x.tokenColors = extend(y.tokenColors, s.extends.replace)
		}

		t[s.id] = x
	}

	/** @type {SyntaxFragment} */
	let f = {tokenColors: []}

	for (let k of Object.keys(t).sort()) {
		let x = t[k]
		f.tokenColors.push(...x.tokenColors)
	}

	return f
}

/**
 * @param {SyntaxConfig[]} sa
 * @returns {SyntaxConfig[]}
 * @throws {Error}
 */
function sort(sa) {
	/** @type {Record<string, string>} */
	let xr = {}

	/** @type {SyntaxConfig[]} */
	let xa = []

	/** @type {Record<string, string>} */
	let yr = {}

	/** @type {SyntaxConfig[]} */
	let ya = []

	for (let s of sa) {
		if (!s.extends && !s.colors) {
			throw new Error(`Syntax Config '${s.id}' has no colors`)
		}

		if (!s.extends) {
			xr[s.id] = s.id
			xa.push(s)
			continue
		}

		yr[s.id] = s.extends.id
		ya.push(s)
	}

	while (ya.length !== 0) {
		for (let i = 0; i < ya.length; i += 1) {
			let s = ya[i]

			let y = yr[s.id]
			let x = xr[y]

			if (!x) {
				continue
			}

			xr[s.id] = s.id
			xa.push(s)

			ya.splice(i, 1)
			i -= 1
		}
	}

	return xa
}

/**
 * @param {SyntaxConfigColors} t
 * @returns {TokenColor[]}
 */
function transform(t) {
	/** @type {Record<string, TokenColor>} */
	let r = {}

	for (let [s, c] of Object.entries(t)) {
		let t = r[c]

		if (!t) {
			t = {scope: [], settings: {foreground: c}}
			r[c] = t
		}

		t.scope.push(s)
	}

	return Object.values(r)
}

/**
 * @param {TokenColor[]} xa
 * @param {[string, string]} replace
 * @returns {TokenColor[]}
 */
function extend(xa, [from, to]) {
	/** @type {TokenColor[]} */
	let ya = []

	let p = new RegExp(`(${from})(\\s+|$)`, "g")

	for (let x of xa) {
		/** @type {TokenColor} */
		let y = {scope: [], settings: x.settings}

		for (let s of x.scope) {
			s = s.replace(p, `${to}$2`)
			y.scope.push(s)
		}

		ya.push(y)
	}

	return ya
}
