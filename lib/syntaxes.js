import * as colors from "./colors.js"

/**
 * @typedef {object} SyntaxTheme
 * @property {S1} comment
 * @property {S2} plain
 * @property {S2} string
 */

/**
 * @typedef {object} SyntaxFragment
 * @property {TokenColor[]} tokenColors
 */

/**
 * @typedef {object} TokenColor
 * @property {string[]} scope
 * @property {TokenColorSettings} settings
 */

/**
 * @typedef {object} TokenColorSettings
 * @property {string} foreground
 */

/**
 * @returns {SyntaxFragment}
 */
export function light() {
	let cs = colors.light()

	/** @type {SyntaxTheme} */
	let st = {
		comment: [cs.gray[4]],
		plain: [cs.gray[5], cs.gray[7]],
		string: [cs.blue[5], cs.blue[7]],
	}

	let ac = configs(st)

	return compute(ac)
}

/**
 * @returns {SyntaxFragment}
 */
export function dark() {
	let cs = colors.dark()

	/** @type {SyntaxTheme} */
	let st = {
		comment: [cs.gray[3]],
		plain: [cs.gray[2], cs.gray[1]],
		string: [cs.slate[2], cs.slate[1]],
	}

	let ac = configs(st)

	return compute(ac)
}

/**
 * @param {SyntaxFragment} sf
 * @returns {SyntaxTheme}
 * @throws {Error}
 */
export function theme(sf) {
	/** @type {SyntaxTheme} */
	let st = {
		comment: [""],
		plain: ["", ""],
		string: ["", ""],
	}

	for (let tc of sf.tokenColors) {
		let c0 = ""
		let p0 = ""
		let p1 = ""
		let s0 = ""
		let s1 = ""

		for (let s of tc.scope) {
			if (s === "comment.block.js") {
				c0 = tc.settings.foreground
				continue
			}

			if (s === "keyword.control.js") {
				p0 = tc.settings.foreground
				continue
			}

			if (s === "source.js") {
				p1 = tc.settings.foreground
				continue
			}

			if (s === "punctuation.definition.string.begin.js") {
				s0 = tc.settings.foreground
				continue
			}

			if (s === "string.quoted.double.js") {
				s1 = tc.settings.foreground
				continue
			}
		}

		if (c0) {
			st.comment[0] = c0
		}

		if (p0) {
			st.plain[0] = p0
		}

		if (p1) {
			st.plain[1] = p1
		}

		if (s0) {
			st.string[0] = s0
		}

		if (s1) {
			st.string[1] = s1
		}

		if (c0 && p0 && p1 && s0 && s1) {
			break
		}
	}

	let c0 = st.comment[0]
	let p0 = st.plain[0]
	let p1 = st.plain[1]
	let s0 = st.string[0]
	let s1 = st.string[1]

	if (!c0 || !p0 || !p1 || !s0 || !s1) {
		throw new Error("Could not restore the syntax theme")
	}

	return st
}

